
# Using pop method
fruits=["apple","kiwi","banana","orenge","grapes"]
fruits.pop()    # it will delete from last
print(fruits)

#Output:- ['apple', 'kiwi', 'banana', 'orenge']

# pop method using position number..
college=['Techno','Jis','MAKAUT','Techono International']
college.pop(1)      # it will delete index 1
print(college)

#Output:-['Techno', 'MAKAUT', 'Techono International']

# Using remove method..
name=['Abhijit','Soumen','Ravi']
name.remove('Soumen')
print(name)

#Output:-['Abhijit', 'Ravi']

#Using dell operator..
laptop=['hp','dell','apple','lenovo']
del laptop[2]
print(laptop)

#Output:-['hp', 'dell', 'lenovo']